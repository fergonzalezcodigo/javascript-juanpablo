/*Diferencia entre funciones y metodos */
const numero1 = 20;
const numero2 = "20";

/*Cuando tienes el nombre sumado a un parentesis, es una función*/
console.log(parseInt(numero2)); //Esto es una función

//Cuando tienes el nombre de la variable seguido de un punto, es un metodo
console.log(numero1.toString()); //Esto es un metodo